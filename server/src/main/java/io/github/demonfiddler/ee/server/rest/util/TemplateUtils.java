package io.github.demonfiddler.ee.server.rest.util;

import java.io.IOException;
import java.io.OutputStream;
import java.io.OutputStreamWriter;
import java.io.Writer;
import java.nio.charset.StandardCharsets;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Component;

import freemarker.template.Configuration;
import freemarker.template.Template;
import freemarker.template.TemplateException;

@Component
public class TemplateUtils {

    @Autowired
    Configuration cfg;

    public Template getTemplateFor(String templateName) throws IOException {
        return cfg.getTemplate(templateName + ".ftl");
    }

    public void render(String templateName, Object root, Writer writer) throws TemplateException, IOException {
        getTemplateFor(templateName).process(root, writer);
    }

    public void render(String templateName, Object root, OutputStream out) throws TemplateException, IOException {
        render(templateName, root, new OutputStreamWriter(out, StandardCharsets.UTF_8));
    }

}
