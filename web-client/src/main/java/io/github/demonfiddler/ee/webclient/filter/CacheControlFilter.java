package io.github.demonfiddler.ee.webclient.filter;

import java.io.IOException;

import jakarta.servlet.Filter;
import jakarta.servlet.FilterChain;
import jakarta.servlet.FilterConfig;
import jakarta.servlet.ServletException;
import jakarta.servlet.ServletRequest;
import jakarta.servlet.ServletResponse;
import jakarta.servlet.http.HttpServletResponse;

public class CacheControlFilter implements Filter {

    private boolean production;

    @Override
    public void init(FilterConfig config) {
        String profile = config.getServletContext().getInitParameter("cacheProfile");
        production = "production".equalsIgnoreCase(profile);
    }

    @Override
    public void doFilter(ServletRequest req, ServletResponse res, FilterChain chain)
            throws IOException, ServletException {

        HttpServletResponse response = (HttpServletResponse) res;

        if (production) {
            response.setHeader("Cache-Control", "max-age=120, must-revalidate");
        } else {
            response.setHeader("Cache-Control", "no-store, no-cache, must-revalidate");
            response.setHeader("Pragma", "no-cache");
            response.setDateHeader("Expires", 0);
        }

        chain.doFilter(req, res);
    }

}
