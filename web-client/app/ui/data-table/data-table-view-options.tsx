'use client'

import { DropdownMenuTrigger } from "@radix-ui/react-dropdown-menu"
import { Table } from "@tanstack/react-table"
import { Settings2 } from "lucide-react"
import { Button } from "@/components/ui/button"
import {
  DropdownMenu,
  DropdownMenuCheckboxItem,
  DropdownMenuContent,
  DropdownMenuLabel,
  DropdownMenuSeparator,
} from "@/components/ui/dropdown-menu"
import RecordKind from "@/app/model/RecordKind"
import ButtonEx from "../ext/button-ex"

export interface DataTableViewOptionsProps<T> {
  table: Table<T>
}

export interface DataTableFilterProps<T> extends DataTableViewOptionsProps<T> {
  recordKind: RecordKind
  recordId: string | undefined
  isLinkableEntity: boolean
  auxRecordId?: string
}

export function DataTableViewOptions<T>({
  table
}: DataTableViewOptionsProps<T>) {
  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <ButtonEx
          variant="ghost"
          size="sm"
          
          className="ml-auto hidden h-8 lg:flex"
          help="Select the columns to display"
        >
          <Settings2 />
          View
        </ButtonEx>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="w-[150px]">
        <DropdownMenuLabel>Toggle columns</DropdownMenuLabel>
        <DropdownMenuSeparator />
        {table
          .getAllColumns()
          .filter(
            (column) =>
              typeof column.accessorFn !== "undefined" && column.getCanHide()
          )
          .map((column) => {
            return (
              <DropdownMenuCheckboxItem
                key={column.id}
                className="capitalize"
                checked={column.getIsVisible()}
                onCheckedChange={(value) => column.toggleVisibility(!!value)}
              >
                {column.id}
              </DropdownMenuCheckboxItem>
            )
          })}
      </DropdownMenuContent>
    </DropdownMenu>
  )
}
